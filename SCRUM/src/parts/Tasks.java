    private void tblItemsTodoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblItemsTodoMouseClicked
        Item item =  new Item();
        Point p = evt.getPoint();
        int row = tblItemsTodo.rowAtPoint(p);
        if ( evt.getClickCount() == 2  ) {
            if (tblItemsTodo.getSelectedRow() >= 0) {
                item = getSelectedItem(tblItemsTodo);
                FrmRegisterItem registerItem = new FrmRegisterItem();
                registerItem.setVisible(true);
                registerItem.setDefaultCloseOperation(WindowConstants.HIDE_ON_CLOSE);

                registerItem.setCurrentItem(item);
                registerItem.editContent();
            }
//            evt.consume();
        }
        
        else
        {
            if (tblItemsTodo.getSelectedRow() >= 0) {
                item = getSelectedItem(tblItemsTodo);
                taskManager.setTasksByItem(tblTasksTodo, sprint, item, 1);
            }
        }
    }//GEN-LAST:event_tblItemsTodoMouseClicked

