    public Item getSelectedItem( JTable table )
    {
        if ( table.getSelectedRow() >= 0 ) {
            return itemManager.getItemById( (Integer) table.getValueAt(table.getSelectedRow() , 0) );
        }
        return null;
    }
    
    public Task getSelectedTask( JTable table )
    {
        if ( table.getSelectedRow() >= 0 ) {
            return taskManager.getTaskById((Integer) table.getValueAt(table.getSelectedRow() , 0) );
        }
        return null;
    }
    
    public Tasks( Sprint sprint )
    {
        initComponents();
        
        this.sprint = sprint;
        
        itemManager.setItemsToDo(tblItemsTodo, "", sprint);
        itemManager.setItemsInProgress(tblItemsWork, "", sprint);
        itemManager.setItemsDone(tblItensDone, "", sprint);
        
        taskManager.setTasks(tblTasksTodo, 1, sprint);
        taskManager.setTasks(tblTasksWork, 2, sprint);
        taskManager.setTasks(tblTasksDone, 3, sprint);
    }
    
    public void setContent( JPanel panel, Sprint sprint )
    {
        this.sprint = sprint;
        
        panel.removeAll();
        
        Tasks tasks = new Tasks( sprint );
        tasks.setVisible( true );
        tasks.setBounds( 0, 0, panel.getWidth(), panel.getHeight() );
        
        panel.add( tasks );
        
        panel.revalidate();
        panel.repaint();
    }
    

