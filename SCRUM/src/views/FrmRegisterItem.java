    private void btnEditActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnEditActionPerformed
    {//GEN-HEADEREND:event_btnEditActionPerformed
        if (tblItem.getSelectedRow() >= 0) {
            id = (Integer) tblItem.getValueAt(tblItem.getSelectedRow(), 0);

            tfdTitle.setText((String) tblItem.getValueAt(tblItem.getSelectedRow(), 1));
            txaInfo.setText((String) tblItem.getValueAt(tblItem.getSelectedRow(), 2));
            String classification = (String) tblItem.getValueAt(tblItem.getSelectedRow(), 3);
            String type = (String) tblItem.getValueAt(tblItem.getSelectedRow(), 4);
            String status = (String) tblItem.getValueAt(tblItem.getSelectedRow(), 5);
            tfdEstimated.setText(String.valueOf((Integer) tblItem.getValueAt(tblItem.getSelectedRow(), 6)));
            tfdDuration.setText(String.valueOf((Integer) tblItem.getValueAt(tblItem.getSelectedRow(), 7)));

            ftfStarted.setText((String) tblItem.getValueAt(tblItem.getSelectedRow(), 8));
            ftfFinished.setText( ( (String) tblItem.getValueAt(tblItem.getSelectedRow(), 9) ).equals("n/d") ? "" : (String) tblItem.getValueAt(tblItem.getSelectedRow(), 9) );

            for (int i = 0; i < cbxSprint.getItemCount(); i++) {
                if (cbxSprint.getItemAt(i).toString().equals((String) tblItem.getValueAt(tblItem.getSelectedRow(), 10))) {
                    cbxSprint.setSelectedIndex(i);
                }

            }

            if (classification.equals("Baixo")) {
                cbxClassification.setSelectedIndex(0);
            } else if (classification.equals("Médio")) {
                cbxClassification.setSelectedIndex(1);
            } else if (classification.equals("Alto")) {
                cbxClassification.setSelectedIndex(2);
            } else {
                cbxClassification.setSelectedIndex(3);
            }

            if (status.equals("TO DO")) {
                cbxStatus.setSelectedIndex(0);
            } else if (status.equals("IN PROGRESS")) {
                cbxStatus.setSelectedIndex(1);
            } else {
                cbxStatus.setSelectedIndex(2);
            }

            if (type.equals("Planejado")) {
                cbxType.setSelectedIndex(0);
            } else {
                cbxType.setSelectedIndex(1);
            }

            tbdItem.setSelectedIndex(0);
        } else {
            JOptionPane.showMessageDialog(null, "Por favor selecione um registro");
        }
    }//GEN-LAST:event_btnEditActionPerformed

