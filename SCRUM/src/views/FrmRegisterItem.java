    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSaveActionPerformed
    {//GEN-HEADEREND:event_btnSaveActionPerformed
        Item item = new Item();

        boolean result = false, validate = false;
        //campos obrigatorios
        if (!(tfdTitle.getText().isEmpty()) && !(txaInfo.getText().equals(""))
                && cbxSprint.getSelectedIndex() != 0) {
            Sprint sprint = sprintTransaction.getSprintByAlias(cbxSprint.getSelectedItem().toString());

            item.setTitle(tfdTitle.getText());
            item.setInfo(txaInfo.getText());
            item.setSprint(sprint);
            if (!tfdDuration.getText().isEmpty()) {
                item.setDuration(tfdDuration.getText());
            } else {
                item.setDuration("0");
            }
            item.setEstimated(tfdEstimated.getText());
            item.setProccessStarted(ftfStarted.getText());

            if (!ftfFinished.getText().isEmpty()) {
                item.setProccessFinished(ftfFinished.getText());
            } else {
                item.setProccessFinished(null);
            }

            //seta o status do item
            if (cbxStatus.getSelectedIndex() == 0) {
                item.setStatus(1);
            } else if (cbxStatus.getSelectedIndex() == 1) {
                item.setStatus(2);
            } else {
                item.setStatus(3);
            }

            //seta a classificação do item
            if (cbxClassification.getSelectedIndex() == 0) {
                item.setClassification("Baixo");
            } else if (cbxClassification.getSelectedIndex() == 1) {
                item.setClassification("Médio");
            } else if (cbxClassification.getSelectedIndex() == 0) {
                item.setClassification("Alto");
            } else {
                item.setClassification("Muito Alto");
            }

            //seta o tipo do item
            if (cbxType.getSelectedIndex() == 0) {
                item.setItemType("Planejado");
            } else {
                item.setItemType("Speed Lane");
            }
            validate = true;
        } else {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos obrigatórios");
            validate = false;
        }

        if (validate && id == 0) {
            result = itemTransaction.insertItem(item);
            itemTransaction.setItemsByFilter(tblItem, "");
        } else if (validate && id > 0) {
            result = itemTransaction.updateItem(id, item);
            itemTransaction.setItemsByFilter(tblItem, "");
        }

        if (validate && result && id == 0) {
            JOptionPane.showMessageDialog(rootPane, "Item inserido com sucesso.");
            clear();
        } else if (validate && result && id > 0) {
            JOptionPane.showMessageDialog(this, "Item atualizado com sucesso");
            clear();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Erro ao inserir ou atualizar item.");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnDelActionPerformed
    {//GEN-HEADEREND:event_btnDelActionPerformed

        if (tblItem.getSelectedRow() >= 0) {
            int id = (Integer) tblItem.getValueAt(tblItem.getSelectedRow(), 0);

            if (itemTransaction.deleteItem(id)) {
                JOptionPane.showMessageDialog(null, "Registro excluido.");
                itemTransaction.setItemsByFilter(tblItem, "");
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao deletar item.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor selecione um registro");
        }
    }//GEN-LAST:event_btnDelActionPerformed

