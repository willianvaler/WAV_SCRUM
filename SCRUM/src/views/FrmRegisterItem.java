    public void editContent() {
        id = currentItem.getIdItem();

        tfdTitle.setText(currentItem.getTitle());
        txaInfo.setText(currentItem.getInfo());
        String classification = currentItem.getClassification();
        String type = currentItem.getItemType();

        tfdEstimated.setText(currentItem.getEstimated());
        tfdDuration.setText(currentItem.getDuration());

        ftfStarted.setText(currentItem.getProccessStarted());
        ftfFinished.setText(currentItem.getProccessFinished());

        for (int i = 0; i < cbxSprint.getItemCount(); i++) {
            if (cbxSprint.getItemAt(i).toString().equals(currentItem.getSprint().getAlias())) {
                cbxSprint.setSelectedIndex(i);
            }

        }

        if (classification.equals("Baixo")) {
            cbxClassification.setSelectedIndex(0);
        } else if (classification.equals("Médio")) {
            cbxClassification.setSelectedIndex(1);
        } else if (classification.equals("Alto")) {
            cbxClassification.setSelectedIndex(2);
        } else {
            cbxClassification.setSelectedIndex(3);
        }

        if (currentItem.getStatus() == 1) {
            cbxStatus.setSelectedIndex(0);
        } else if (currentItem.getStatus() == 2) {
            cbxStatus.setSelectedIndex(1);
        } else {
            cbxStatus.setSelectedIndex(3);
        }

        if (type.equals("Planejado")) {
            cbxType.setSelectedIndex(0);
        } else {
            cbxType.setSelectedIndex(1);
        }

        tbdItem.setSelectedIndex(0);
    }

