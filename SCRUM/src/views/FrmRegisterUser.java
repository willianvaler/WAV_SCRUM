    private void btnEditActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnEditActionPerformed
    {//GEN-HEADEREND:event_btnEditActionPerformed
         if ( tblUser.getSelectedRow() >= 0 )
        {
            id = (Integer) tblUser.getValueAt( tblUser.getSelectedRow(), 0);
            ftfPhone.setText( (String) tblUser.getValueAt( tblUser.getSelectedRow(), 3) );
            pfd1.setText( (String) tblUser.getValueAt( tblUser.getSelectedRow(), 5 ) );
            pfd2.setText( (String) tblUser.getValueAt( tblUser.getSelectedRow(), 5 ) );
            tfdEmail.setText( (String) tblUser.getValueAt( tblUser.getSelectedRow(), 2 ) );
            tfdName.setText( (String) tblUser.getValueAt( tblUser.getSelectedRow(), 1 ) );
            int type = (Integer) tblUser.getValueAt( tblUser.getSelectedRow(), 6);
            // set cbx selected index
            if ( type == 2)
            {
                cbxUserType.setSelectedIndex( 0 );
            } 
            else if ( type == 3)
            {
                cbxUserType.setSelectedIndex( 1 );
            } 
            else if ( type == 4 )
            {
                cbxUserType.setSelectedIndex( 2 );
            } 
            else
            {
                cbxUserType.setSelectedIndex( 3);
            }
            tfdlogin.setText( (String) tblUser.getValueAt( tblUser.getSelectedRow(), 4 ) );
            
            Address a = userTransactions.getAddressByLogin( (String) tblUser.getValueAt( tblUser.getSelectedRow(), 4 ) );
            
            if ( a != null )
            {
                tfdAddress.setText( a.getAddress() );
                tfdCity.setText( a.getCity() );
                tfdDistrict.setText( a.getDistrict() );
                tfdState.setText( a.getState());
                tfdUf.setText( a.getUf() );
                idAddress = a.getIdAddress();
            }
            else
            {
                tfdAddress.setText( "" );
                tfdCity.setText( "" );
                tfdDistrict.setText( "" );
                tfdState.setText( "" );
                tfdUf.setText( "" );
            }
            
            tbdUser.setSelectedIndex( 0 );
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Por favor selecione um registro");
        }
    }//GEN-LAST:event_btnEditActionPerformed

