    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSaveActionPerformed
    {//GEN-HEADEREND:event_btnSaveActionPerformed
        User user = new User();
        Address address = new Address();
        boolean validate;
        if ( !tfdName.getText().equals( "" ) && !tfdEmail.getText().equals( "" ) && 
                !tfdlogin.getText().equals( "" ) && 
                !pfd1.getText().equals( "" ) && !pfd2.getText().equals( "" )  )  
        {
            user.setName( tfdName.getText() );
            user.setEmail( tfdEmail.getText() );
            user.setPhone( ftfPhone.getText() );
            user.setLogin( tfdlogin.getText() );
            user.setPassword( pfd2.getText() );
            int userType ;
            if (cbxUserType.getSelectedIndex() == 0) {
                userType = 2;
            }
            else if (cbxUserType.getSelectedIndex() == 1) {
                userType = 3;
            }
            else if (cbxUserType.getSelectedIndex() == 2) {
                userType = 4;
            }
            else {
                userType = 5;
            }
                
            user.setUserType( userType );
            
            address.setAddress( tfdAddress.getText() );
            address.setDistrict( tfdDistrict.getText() );
            address.setCity( tfdCity.getText() );
            address.setState( tfdState.getText() );
            address.setUf( tfdUf.getText() );
            address.setIdAddress(idAddress);
            validate = true;
        }
        else
        {
            JOptionPane.showMessageDialog( this, "Os campos: Nome, login, E-mail, Login, Senha e Tipo não podem estar em branco" );
            validate = false;
        }
        boolean  validatePWD = false, validateLogin = false, result= false;
        
        //valida se o login já existe no banco
        validateLogin = userTransactions.validateDuplicatedUserByLogin( user );
        if ( !validateLogin && id == 0)
        {
            JOptionPane.showMessageDialog( this, "Login já existe." );
            validateLogin = false;
            
        }
        
        //caso senhas não seja iguais
        if( pfd1.getText().equals( pfd2.getText()) )
        {
            validatePWD = true;
        }
        else
        {
            validatePWD = false;
        }
        
        if ( tblTeam.getSelectedRow() >= 0 ) 
        {
            user.setRefTeam( (Integer) tblTeam.getValueAt( tblTeam.getSelectedRow() , 0 ) );
        }
        
        if ( validatePWD && validateLogin && id == 0 && validate )
        {
            result = userTransactions.insertUser( user, address );
            userTransactions.setItensByFilter( tblUser, "" );
            teamTransactions.setItensByFilter(tblTeam, "");
            teamTransactions.updateTotalParticipants( user.getRefTeam() );
            clear();
        }
        
        else if( id > 0 )
        {
            result = userTransactions.updateUser( id , user, address );
            userTransactions.setItensByFilter( tblUser, "" );
            teamTransactions.setItensByFilter(tblTeam, "");
            clear();
        }
        
        
        if( result && id == 0)
        {
            JOptionPane.showMessageDialog( rootPane, "Usuário inserido com sucesso." );
            id = 0;
        }
        
        else if ( id > 0 )
        {
            JOptionPane.showMessageDialog(this, "Usuário atualizado atualizado com sucesso" );
            id = 0;
        }
        
        else
        {
            JOptionPane.showMessageDialog( rootPane, "Erro ao inserir ou atualizar usuário.");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

