    private void btnDelActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnDelActionPerformed
    {//GEN-HEADEREND:event_btnDelActionPerformed
        
        if ( tblUser.getSelectedRow() >= 0 )
        {
            int id = (Integer) tblUser.getValueAt( tblUser.getSelectedRow(), 0);
            
            if ( userTransactions.deleteUser( id ) )
            {
                teamTransactions.decreaseTotalParticipants( (Integer) tblUser.getValueAt( tblUser.getSelectedRow(), 7 ) );
                JOptionPane.showMessageDialog( null, "Registro excluido.");
                userTransactions.setItensByFilter( tblUser, "" );
            }
            else
            {
                JOptionPane.showMessageDialog( null, "Erro ao deletar usuário." );
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Por favor selecione um registro");
        }
    }//GEN-LAST:event_btnDelActionPerformed

